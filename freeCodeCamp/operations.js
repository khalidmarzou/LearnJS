// Adding Numbers :
let sum = 10 + 10;
console.log(sum); // 20
// Subtracting :
let difference = 45 - 33;
console.log(difference); // 12
// Multipliying Numbers :
var mult = 8 * 10;
console.log(mult); // 80
// Dividing Numbers :
let quotient = 66 / 33;
console.log(quotient); // 2
// Incrementing Numbers :
let myVar = 87;
myVar++; // myVar = myVar + 1;
console.log(myVar); // 88
// Decrementing Numbers :
let myVarr = 11;
myVarr--; // or myVarr = myVarr - 1
console.log(myVarr); // 10
// Decimal Numbers :
let ourDecimal = 5.7; // float
let myDecimal = 0.009; // float
// Multiply Decimals :
let product = 2.0 * 2.5;
console.log(product); // 5
// Devide Decimals :
let q = 4.4 / 2.0;
console.log(q); // 2.2
// Finding a Remainder :
let remainder;
remainder = 11 % 3;
console.log(remainder) // 2
